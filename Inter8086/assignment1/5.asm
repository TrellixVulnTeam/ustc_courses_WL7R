	BUFFER DB "STUD", '$'
DATA	ENDS
CODE	SEGMENT
	ASSUME CS:CODE, DS:DATA
START:	MOV AX, DATA
	MOV DS, AX
	MOV SI, OFFSET BUFFER
	XOR CL, CL
NEXT:	MOV AL, [SI]
	CMP AL, '$'
	JZ FINAL
	INC CL
	INC SI
	JMP NEXT
FINAL:	MOV DL, CL
	AND DL, 0F0H
	ROR DL, 4
	ADD DL, 30H
	MOV AH, 02H
	INT 21H
	MOV DL, CL
	AND DL, 0FH
	ADD DL, 30H
	MOV AH, 02H
	INT 21H
	MOV AX, 4C00H
	INT 21H
CODE	ENDS
	END START
	